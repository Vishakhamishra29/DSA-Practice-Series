public class Move_ZeroesToEnd {
    public static void moveZeroesto(int[] arr){
        int j=0;
        for(int i=0;i<arr.length;i++){
        if(arr[i]!=0){
            int temp=arr[i];
            arr[i]=arr[j];
            arr[j]=temp;
            j++;
        }
        }
    }
    public static void main(String[] args) {
        int[] arr = {0,1,4,0,5,2};
        moveZeroesto(arr);
        for (int i=0;i<arr.length;i++) {
            System.out.print(arr[i] + " ");
        }
    }
}

